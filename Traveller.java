import java.time.LocalDate;

class Traveller 
{
    private String Name;
    private long mobile;
    private String email;
    private String flightId;
    private LocalDate date;
	public Traveller(String name, long mobile, String email, String flightId, LocalDate date)
	{
		this.Name = name;
		this.mobile = mobile;
		this.email = email;
		this.flightId = flightId;
		this.date = date;
	}
	
	
	  }
    

